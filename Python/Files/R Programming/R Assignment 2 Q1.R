Here's a simple R program to compute the sum, mean, and product of elements in a given vector:

R
# Define a vector
vector <- c(1, 2, 3, 4, 5)

# Compute sum of elements
sum_result <- sum(vector)

# Compute mean of elements
mean_result <- mean(vector)

# Compute product of elements
product_result <- prod(vector)

# Print results
cat("Sum:", sum_result, "\n")
cat("Mean:", mean_result, "\n")
cat("Product:", product_result, "\n")


You can replace the `vector` variable with any vector you want to compute the sum, mean, and product for.
